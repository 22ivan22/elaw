package hr.fname.elaw.model;

public class Country {
	
	Integer id;
	Integer code;
	String codeC;
	String nameCro;
	String nameLongCro;
	String nameEng;
	String nameLongEng;
	String mark2;
	String mark3;
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public Integer getCode() {
		return code;
	}
	public void setCode(Integer code) {
		this.code = code;
	}
	public String getCodeC() {
		return codeC;
	}
	public void setCodeC(String codeC) {
		this.codeC = codeC;
	}
	public String getNameCro() {
		return nameCro;
	}
	public void setNameCro(String nameCro) {
		this.nameCro = nameCro;
	}
	public String getNameLongCro() {
		return nameLongCro;
	}
	public void setNameLongCro(String nameLongCro) {
		this.nameLongCro = nameLongCro;
	}
	public String getNameEng() {
		return nameEng;
	}
	public void setNameEng(String nameEng) {
		this.nameEng = nameEng;
	}
	public String getNameLongEng() {
		return nameLongEng;
	}
	public void setNameLongEng(String nameLongEng) {
		this.nameLongEng = nameLongEng;
	}
	public String getMark2() {
		return mark2;
	}
	public void setMark2(String mark2) {
		this.mark2 = mark2;
	}
	public String getMark3() {
		return mark3;
	}
	public void setMark3(String mark3) {
		this.mark3 = mark3;
	}
	
	

}
